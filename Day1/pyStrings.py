# Getting Started with Strings
text = "this is some textual data here"

print(text)                                 # here we have the output for it

# getting particular partition from the string using slicing and indexes
print(text[3])

# the above one will output just the first 3 characters from the string
# getting another partition for the data
print(text[14:])                                # all the string part after 14 position

# we can also define the strings here using '' single quotes
print('This is another string with single quotes')

# Here let us create a number and add it to a string
print("The number here is : " + str(45))